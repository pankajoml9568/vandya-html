<script>
    $(function(){
        $('.enquiry-form').submit(function(event){
            event.preventDefault();
            var form = $(this);
            $.ajax({
                type: "POST",
                url: "php/save-enquiry.php",
                dataType: "json",
                async: false,
                data: form.serialize(),
                beforeSend: function() {
                    $('.sub_loader').fadeIn();
                },
                success: function (data) {

                    if(data.status == "success") {
                        $('.scss-msg').text(data.message);
                        $('.scss-div').fadeIn();
                        $('.err-div').fadeOut();
                        form.siblings('input, textarea, select').val('');
                        $( '.enquiry-form' ).reset();
                    }

                    if(data.status == "error") {
                        $('.err-msg').text(data.message);
                        $('.err-div').fadeIn();
                        $('.scss-div').fadeOut();
                    }
                },
                error: function (textStatus, errorThrown) {
                        $('.err-msg').text('Somthing went wrong!');
                        $('.err-div').fadeIn();
                        $('.scss-div').fadeOut();
                }
            });

            $('.sub_loader').fadeOut();

        });
    });

    function onlyNumberKey(evt) {
        // Only ASCII character in that range allowed
        var ASCIICode = (evt.which) ? evt.which : evt.keyCode
        if (ASCIICode > 31 && (ASCIICode < 48 || ASCIICode > 57))
            return false;
        return true;
    }

