<script>
    $(function(){
        var hash = location.hash;
        
        if(hash) {
            goToHash(hash);
        }

        $(window).on('hashchange', function() {
            var hash = location.hash;
            if(hash=="#assembly" || hash=="#circletime" || hash=="#yoga") {
                goToHash(hash);
            }
        });
    })

    function goToHash(hash) {
        $("[data-id='"+hash+"']").trigger('click');
        $('html, body').animate({
            scrollTop: $("[data-id='"+hash+"']").offset().top
        });
    }
</script>