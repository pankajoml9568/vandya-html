<script>
    $(function(){
        var hash = location.hash;
        
        if(hash) {
            goToHash(hash);
        }

        $(window).on('hashchange', function() {
            var hash = location.hash;
            if(hash=="#sports" || hash=="#adventure" || hash=="#personalsafety" || hash=="#healtheducation" || hash=="#sexeducation") {
                goToHash(hash);
            }
        });
    })

    function goToHash(hash) {
        $("[data-id='"+hash+"']").trigger('click');
        $('html, body').animate({
            scrollTop: $("[data-id='"+hash+"']").offset().top
        });
    }
</script>