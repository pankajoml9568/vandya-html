<script>
$(function() {
    var hash = location.hash;

    if (hash) {
        goToHash(hash);
    }

    $(window).on('hashchange', function() {
        var hash = location.hash;
        if (hash == "#Ankuran" || hash == "#Bodh" || hash == "#Srijan" || hash == "#Ojal") {
            goToHash(hash);
        }
    });
})

function goToHash(hash) {
    $("[data-id='" + hash + "']").find('.our-school-tab').trigger('click');
    $('html, body').animate({
        scrollTop: $("[data-id='" + hash + "']").offset().top
    });
}
