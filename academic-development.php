<script>
    $(function(){
        var hash = location.hash;
        
        if(hash) {
            goToHash(hash);
        }

        $(window).on('hashchange', function() {
            var hash = location.hash;
            if(hash=="#literacy" || hash=="#digital" || hash=="#performingarts" || hash=="#careereducation") {
                goToHash(hash);
            }
        });
    })

    function goToHash(hash) {
        $("[data-id='"+hash+"']").find('.nav-link').trigger('click');
        $('html, body').animate({
            scrollTop: $("[data-id='"+hash+"']").offset().top
        });
    }
</script>